
public class Employee {
	
	private int empId;
	private String empName;
	private double empSalory;
	public Employee(int empId, String empName, double empSalory) {
		super();
		this.empId = empId;
		this.empName = empName;
		this.empSalory = empSalory;
	}
	public int getEmpId() {
		return empId;
	}
	public String getEmpName() {
		return empName;
	}
	public double getEmpSalory() {
		return empSalory;
	}
	
	

}
